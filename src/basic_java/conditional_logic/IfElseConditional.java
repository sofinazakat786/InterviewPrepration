package basic_java.conditional_logic;

public class IfElseConditional {
    public static void main(String[] args) {
        int value1 = 10;
        int value2 = 4;
        if (value1 > value2)
            System.out.println("Value1: " + value1 + " is bigger than value2: " + value2);
        else
            System.out.println("Value 2 is bigger.");

    }
}
